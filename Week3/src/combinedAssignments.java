public class combinedAssignments{
    public static void main(String[] args) {
        int totalValue = 10;

        totalValue += 2000;

        System.out.println(totalValue);

        totalValue += 10;

        System.out.println(totalValue);

        totalValue -= 420;

        System.out.println(totalValue);

        totalValue *= 15;

        System.out.println(totalValue);

        totalValue /= 22;

        System.out.println(totalValue);

        totalValue %= 13;

        System.out.println(totalValue);
    }
}